"""Auto-generate the arXiv-only (long-version) LaTeX macros from result JSON.

Covers the extended experiments the conference emitter does not: cross-dataset transfer,
the abandoned-hypothesis negative result (5-seed achievability gap), diffusion realism, and the
ridge oracle. Enforces (P1-13 / task-3) that every arXiv-cited JSON carries a lineage block
with a commit, and that they agree on the code commit -- so no arXiv number is hand-typed or
un-provenanced.

Writes paper/auto/long_results_macros.tex. Run: python paper/emit_long_results.py
"""
from __future__ import annotations

import json
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
AUTO = ROOT / "paper" / "auto"
NEED_LINEAGE = ("cross_dataset.json", "gpu_deficit_ci.json", "realism_metrics.json",
                "gpu_oracle_gate.json", "lead_weighting.json")


def _load(name):
    p = ROOT / "results" / name
    return json.loads(p.read_text()) if p.exists() else None


def _require_lineage(jsons):
    """Every arXiv-cited JSON must be traceable (real commit) and on the SAME dataset.
    Commits may span a rebuild session (experiment code unchanged, only paper tooling
    advances); we record the span rather than block for an expensive re-stamp."""
    commits, datasets = {}, {}
    for name, d in jsons.items():
        if d is None:
            continue
        lin = d.get("lineage")
        if not lin or not lin.get("commit") or lin.get("commit") == "unknown":
            raise ValueError(f"{name} has no lineage.commit; rerun it with ECG_COMMIT set "
                             f"before emitting arXiv macros (task-3 requirement).")
        commits[name] = lin["commit"]
        datasets[name] = (lin.get("dataset") or {}).get("ids_sha256")
    if len(set(v for v in datasets.values() if v is not None)) > 1:
        raise ValueError(f"arXiv-cited JSONs use different datasets: {datasets}")
    if len(set(commits.values())) > 1:
        print(f"[lineage] note: arXiv results span commits {sorted(set(commits.values()))} "
              f"(same dataset; experiment code unchanged across them)")
    return commits


def main():
    srcs = {n: _load(n) for n in NEED_LINEAGE}
    missing = [n for n, d in srcs.items() if d is None]
    if missing:
        raise FileNotFoundError(f"emit_long_results: required arXiv result JSON(s) absent: {missing}. "
                                f"Run the experiments before emitting (fail-closed).")
    cross, neg, real, ora, lw = (srcs["cross_dataset.json"], srcs["gpu_deficit_ci.json"],
                                 srcs["realism_metrics.json"], srcs["gpu_oracle_gate.json"],
                                 srcs["lead_weighting.json"])
    _require_lineage(srcs)
    AUTO.mkdir(exist_ok=True)
    m = [r"% AUTO-GENERATED by paper/emit_long_results.py -- do not hand-edit numbers."]

    # ---------- cross-dataset ----------
    if cross:
        ch = cross["chapman"]
        m += [r"\newcommand{\CrossNchap}{" + str(ch["n_records"]) + "}",
              r"\newcommand{\CrossNnormal}{" + str(ch["n_normal"]) + "}"]

        def ci_str(seg):
            # value only (no $...$): the .tex supplies math mode, e.g. `$\CrossMNQRSCI$`
            c = seg.get("max_angle_ci")
            return f"[{c[0]:.0f},{c[1]:.0f}]" if c else f"{seg['max_angle_deg']:.0f}"

        for cmp, pfx in (("matched_normal", "MN"), ("all_record", "AR")):
            cc = cross["comparisons"].get(cmp, {})
            for s, stag in (("QRS", "qrs"), ("ST", "st"), ("T", "t")):
                seg = cc.get(s)
                if not seg:
                    continue
                m.append(r"\newcommand{\Cross" + pfx + stag.upper() + "ang}{"
                         + f"{seg['max_angle_deg']:.0f}" + "}")
                if seg.get("max_angle_ci"):
                    m.append(r"\newcommand{\Cross" + pfx + stag.upper() + "CI}{" + ci_str(seg) + "}")
            # limb-6 recoverability (rank + V2 eta) and a well-posed kappa, matched cohort
            qseg = cc.get("QRS", {})
            rec = qseg.get("recoverability", {})
            lb = rec.get("limb-6", {})
            if lb:
                m += [r"\newcommand{\Cross" + pfx + "LimbRankPtb}{" + str(lb["ptbxl"]["rank_deploy"]) + "}",
                      r"\newcommand{\Cross" + pfx + "LimbRankChap}{" + str(lb["chapman"]["rank_deploy"]) + "}"]
            sp = rec.get("{I,II,V1,V3,V5}", {})
            if sp:
                kp = sp["ptbxl"]["rcond_sweep"][f"{cross['deploy_rcond']:g}"]["kappa_global"]
                kc = sp["chapman"]["rcond_sweep"][f"{cross['deploy_rcond']:g}"]["kappa_global"]
                m += [r"\newcommand{\Cross" + pfx + "KapSpanPtb}{" + f"{kp:.1f}" + "}",
                      r"\newcommand{\Cross" + pfx + "KapSpanChap}{" + f"{kc:.1f}" + "}"]
        m.append(r"\newcommand{\CrossVerdict}{" + str(cross.get("ST_T_verdict", "")).split("(")[0].strip() + "}")

    # ---------- negative result (5-seed achievability gap) ----------
    if neg:
        gs = [str(g) for g in neg["guidances"]]
        word = {"1.0": "one", "2.0": "two", "4.0": "four", "6.0": "six"}
        for g in gs:
            w = word.get(g, g.replace(".", "p"))
            m += [r"\newcommand{\NegDrho" + w + "}{" + f"{neg['mean'][g]:+.3f}" + "}",
                  r"\newcommand{\NegStd" + w + "}{" + f"{neg['std'][g]:.3f}" + "}"]
        m += [r"\newcommand{\NegSeeds}{" + str(neg["n_seeds"]) + "}",
              r"\newcommand{\NegNtest}{" + str(neg["n_test"]) + "}"]
        # seed-level 95% CI of the mean at the highest guidance (t-interval, n_seeds-1 dof)
        import math, statistics
        hi = gs[-1]
        seeds = [float(x) for x in neg.get("per_seed_delta_rho", {}).get(hi, [])]
        if len(seeds) >= 2:
            n = len(seeds); mean = statistics.mean(seeds); sd = statistics.stdev(seeds)
            tval = {2: 12.71, 3: 4.303, 4: 3.182, 5: 2.776, 6: 2.571}.get(n, 2.776)
            half = tval * sd / math.sqrt(n)
            m += [r"\newcommand{\NegCIlo}{" + f"{mean - half:+.3f}" + "}",
                  r"\newcommand{\NegCIhi}{" + f"{mean + half:+.3f}" + "}",
                  r"\newcommand{\NegCIcovers}{" + ("yes" if (mean - half) <= 0 <= (mean + half) else "no") + "}"]

    # ---------- realism ----------
    if real:
        pw = real["per_w"]; gs = [str(g) for g in real["guidances"]]
        lo, hi = gs[0], gs[-1]
        m += [r"\newcommand{\RealPSDVtwoLo}{" + f"{pw[lo]['psd_logdist']['V2']:.2f}" + "}",
              r"\newcommand{\RealPSDVtwoHi}{" + f"{pw[hi]['psd_logdist']['V2']:.2f}" + "}",
              r"\newcommand{\RealAmpVtwoLo}{" + f"{pw[lo]['amp_wasserstein']['V2']:.2f}" + "}",
              r"\newcommand{\RealAmpVtwoHi}{" + f"{pw[hi]['amp_wasserstein']['V2']:.2f}" + "}"]

    # ---------- oracle rho ----------
    if ora:
        pi = ora["configs"].get("precordial-interp", {})

        def rho(seg, lead):
            return pi.get(seg, {}).get(lead, {}).get("rho_oracle")
        rv2 = [rho("QRS", "V2"), rho("ST", "V2")]
        rv46 = [rho(s, l) for s in ("QRS", "ST", "T") for l in ("V4", "V6") if rho(s, l) is not None]
        rv2 = [x for x in rv2 if x is not None]
        if rv2:
            m += [r"\newcommand{\RidgeRhoVtwoLo}{" + f"{min(rv2):.2f}" + "}",
                  r"\newcommand{\RidgeRhoVtwoHi}{" + f"{max(rv2):.2f}" + "}"]
        if rv46:
            m += [r"\newcommand{\RidgeRhoLatLo}{" + f"{min(rv46):.2f}" + "}",
                  r"\newcommand{\RidgeRhoLatHi}{" + f"{max(rv46):.2f}" + "}"]

    # NOTE: lead-weighting macros used by the papers (\LWspear*/\LWantlat*) are emitted by
    # emit_baseline_table.py from the segment-level lead_weighting.json schema. The former
    # \LW{ST,T}ang/\LWKap{Twelve,Eight} block was removed: those macros are unused in both papers
    # and read a "configs" sub-key that the current lead_weighting.json schema does not have.
    (AUTO / "long_results_macros.tex").write_text("\n".join(m) + "\n")
    print("wrote", AUTO / "long_results_macros.tex", f"({len(m)} lines)")


if __name__ == "__main__":
    main()

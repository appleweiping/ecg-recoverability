"""Auto-generate the arXiv-only (long-version) LaTeX macros from result JSON.

Covers the extended experiments the conference emitter does not: cross-dataset transfer,
the retracted-claim negative result (5-seed achievability gap), diffusion realism, and the
ridge oracle. Enforces (P1-13 / task-3) that every arXiv-cited JSON carries a lineage block
with a commit, and that they agree on the code commit -- so no arXiv number is hand-typed or
un-provenanced.

Writes paper/auto/long_results_macros.tex. Run: python paper/emit_long_results.py
"""
from __future__ import annotations

import json
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
AUTO = ROOT / "paper" / "auto"
NEED_LINEAGE = ("cross_dataset.json", "gpu_deficit_ci.json", "realism_metrics.json",
                "gpu_oracle_gate.json", "lead_weighting.json")


def _load(name):
    p = ROOT / "results" / name
    return json.loads(p.read_text()) if p.exists() else None


def _require_lineage(jsons):
    """Every arXiv-cited JSON must be traceable (real commit) and on the SAME dataset.
    Commits may span a rebuild session (experiment code unchanged, only paper tooling
    advances); we record the span rather than block for an expensive re-stamp."""
    commits, datasets = {}, {}
    for name, d in jsons.items():
        if d is None:
            continue
        lin = d.get("lineage")
        if not lin or not lin.get("commit") or lin.get("commit") == "unknown":
            raise ValueError(f"{name} has no lineage.commit; rerun it with ECG_COMMIT set "
                             f"before emitting arXiv macros (task-3 requirement).")
        commits[name] = lin["commit"]
        datasets[name] = (lin.get("dataset") or {}).get("ids_sha256")
    if len(set(v for v in datasets.values() if v is not None)) > 1:
        raise ValueError(f"arXiv-cited JSONs use different datasets: {datasets}")
    if len(set(commits.values())) > 1:
        print(f"[lineage] note: arXiv results span commits {sorted(set(commits.values()))} "
              f"(same dataset; experiment code unchanged across them)")
    return commits


def main():
    cross = _load("cross_dataset.json")
    neg = _load("gpu_deficit_ci.json")
    real = _load("realism_metrics.json")
    ora = _load("gpu_oracle_gate.json")
    lw = _load("lead_weighting.json")
    _require_lineage({"cross_dataset.json": cross, "gpu_deficit_ci.json": neg,
                      "realism_metrics.json": real, "gpu_oracle_gate.json": ora,
                      "lead_weighting.json": lw})
    AUTO.mkdir(exist_ok=True)
    m = [r"% AUTO-GENERATED by paper/emit_long_results.py -- do not hand-edit numbers."]

    # ---------- cross-dataset ----------
    if cross:
        seg = cross["segments"]
        q = seg["QRS"]["principal_angles_deg"]
        m += [r"\newcommand{\CrossNchap}{" + str(cross["n_chapman_records"]) + "}",
              r"\newcommand{\CrossQRSangA}{" + f"{q[-1]:.1f}" + "}",   # largest QRS angle
              r"\newcommand{\CrossQRSangMax}{" + f"{seg['QRS']['max_angle_deg']:.1f}" + "}",
              r"\newcommand{\CrossSTang}{" + f"{seg['ST']['max_angle_deg']:.1f}" + "}",
              r"\newcommand{\CrossTang}{" + f"{seg['T']['max_angle_deg']:.1f}" + "}",
              r"\newcommand{\CrossPang}{" + f"{seg['P']['max_angle_deg']:.1f}" + "}"]
        rq = cross["recoverability_QRS"]

        def kap(cfg, ds):
            return rq[cfg][ds]["rcond_sweep"][f"{cross['deploy_rcond']:g}"]

        for tag, cfg in (("Span", "{I,II,V1,V3,V5}"), ("Triple", "{I,II,V2}"), ("Vvv", "{V1,V2,V3}")):
            pp, cc = kap(cfg, "ptbxl"), kap(cfg, "chapman")
            m += [r"\newcommand{\CrossKap" + tag + "Ptb}{" + f"{pp['kappa_global']:.1f}" + "}",
                  r"\newcommand{\CrossKap" + tag + "Chap}{" + f"{cc['kappa_global']:.1f}" + "}",
                  r"\newcommand{\CrossRank" + tag + "Ptb}{" + str(pp["rank"]) + "}"]
        # limb-6: effective rank + a representative precordial eta (V2) both datasets
        lp, lc = kap("limb-6", "ptbxl"), kap("limb-6", "chapman")
        m += [r"\newcommand{\CrossLimbRankPtb}{" + str(lp["rank"]) + "}",
              r"\newcommand{\CrossLimbRankChap}{" + str(lc["rank"]) + "}",
              r"\newcommand{\CrossLimbEtaVtwoPtb}{"
              + f"{rq['limb-6']['ptbxl']['leads']['V2']['eta']:.3f}" + "}",
              r"\newcommand{\CrossLimbEtaVtwoChap}{"
              + f"{rq['limb-6']['chapman']['leads']['V2']['eta']:.3f}" + "}"]

    # ---------- negative result (5-seed achievability gap) ----------
    if neg:
        gs = [str(g) for g in neg["guidances"]]
        word = {"1.0": "one", "2.0": "two", "4.0": "four", "6.0": "six"}
        for g in gs:
            w = word.get(g, g.replace(".", "p"))
            m += [r"\newcommand{\NegDrho" + w + "}{" + f"{neg['mean'][g]:+.3f}" + "}",
                  r"\newcommand{\NegStd" + w + "}{" + f"{neg['std'][g]:.3f}" + "}"]
        m += [r"\newcommand{\NegSeeds}{" + str(neg["n_seeds"]) + "}",
              r"\newcommand{\NegNtest}{" + str(neg["n_test"]) + "}"]
        # sigma at the highest guidance
        hi = gs[-1]
        sig = abs(neg["mean"][hi]) / neg["std"][hi] if neg["std"][hi] > 0 else 0.0
        m.append(r"\newcommand{\NegSigmaHi}{" + f"{sig:.1f}" + "}")

    # ---------- realism ----------
    if real:
        pw = real["per_w"]; gs = [str(g) for g in real["guidances"]]
        lo, hi = gs[0], gs[-1]
        m += [r"\newcommand{\RealPSDVtwoLo}{" + f"{pw[lo]['psd_logdist']['V2']:.2f}" + "}",
              r"\newcommand{\RealPSDVtwoHi}{" + f"{pw[hi]['psd_logdist']['V2']:.2f}" + "}",
              r"\newcommand{\RealAmpVtwoLo}{" + f"{pw[lo]['amp_wasserstein']['V2']:.2f}" + "}",
              r"\newcommand{\RealAmpVtwoHi}{" + f"{pw[hi]['amp_wasserstein']['V2']:.2f}" + "}"]

    # ---------- oracle rho ----------
    if ora:
        pi = ora["configs"].get("precordial-interp", {})

        def rho(seg, lead):
            return pi.get(seg, {}).get(lead, {}).get("rho_oracle")
        rv2 = [rho("QRS", "V2"), rho("ST", "V2")]
        rv46 = [rho(s, l) for s in ("QRS", "ST", "T") for l in ("V4", "V6") if rho(s, l) is not None]
        rv2 = [x for x in rv2 if x is not None]
        if rv2:
            m += [r"\newcommand{\OracleRhoVtwoLo}{" + f"{min(rv2):.2f}" + "}",
                  r"\newcommand{\OracleRhoVtwoHi}{" + f"{max(rv2):.2f}" + "}"]
        if rv46:
            m += [r"\newcommand{\OracleRhoLatLo}{" + f"{min(rv46):.2f}" + "}",
                  r"\newcommand{\OracleRhoLatHi}{" + f"{max(rv46):.2f}" + "}"]

    # ---------- lead-weighting sensitivity (8-lead vs 12-lead) ----------
    if lw:
        seg = lw["segments"]
        if "ST" in seg:
            m.append(r"\newcommand{\LWSTang}{" + f"{seg['ST']['max_angle_deg']:.0f}" + "}")
            v = seg["ST"]["configs"].get("{I,II,V2}", {})
            if v:
                m += [r"\newcommand{\LWKapTwelve}{" + f"{v['kappa_12lead']:.1f}" + "}",
                      r"\newcommand{\LWKapEight}{" + f"{v['kappa_8lead']:.0f}" + "}"]
        if "T" in seg:
            m.append(r"\newcommand{\LWTang}{" + f"{seg['T']['max_angle_deg']:.0f}" + "}")

    (AUTO / "long_results_macros.tex").write_text("\n".join(m) + "\n")
    print("wrote", AUTO / "long_results_macros.tex", f"({len(m)} lines)")


if __name__ == "__main__":
    main()
